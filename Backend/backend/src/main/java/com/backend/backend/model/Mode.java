package com.backend.backend.model;


/**
 * Enumeration representing different game modes.
 * It defines the possible modes a game can have, such as "1v1" (one versus one) and "2v2" (two versus two).
 */
public enum Mode {
    ONEVONE, // Represents the "1v1" game mode, where two players compete against each other.
    TWOVTWO  // Represents the "2v2" game mode, where two teams of two players each compete.
}

